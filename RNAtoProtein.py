def rna_to_protein(rna_string):
    protein = ""
    for i in range(0, len(rna_string), 3):
        codon = rna_string[i:i+3]
        amino_acid = CODON_TABLE.get(codon, "")
        if amino_acid == "Stop":
            break
        protein += amino_acid
    return protein

